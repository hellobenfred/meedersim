local PROFILE_DEFAULTS = {
    overall = DEFAULT_BIS_OVERALL,
    raid = DEFAULT_BIS_RAID,
    mplus = DEFAULT_BIS_MPLUS,
}

function MeederSIM:InitBiS()
    if not MeederSIMCharDB.bisProfiles then
        MeederSIMCharDB.bisProfiles = { overall = {}, raid = {}, mplus = {}, personal = {} }
    end
    if not MeederSIMCharDB.bisProfiles.personal then
        MeederSIMCharDB.bisProfiles.personal = {}
    end
    for _, key in ipairs({"overall", "raid", "mplus"}) do
        if not MeederSIMCharDB.bisProfiles[key] then
            MeederSIMCharDB.bisProfiles[key] = {}
        end
    end
    if not MeederSIMCharDB.bisLoaded3 and self.class and self.spec then
        for _, key in ipairs({"overall", "raid", "mplus"}) do
            local defs = PROFILE_DEFAULTS[key]
            if defs then
                local cd = defs[self.class]
                local sd = cd and cd[self.spec]
                if sd then
                    local p = MeederSIMCharDB.bisProfiles[key]
                    for sid, iid in pairs(sd) do
                        if not p[sid] then
                            local n = GetItemInfo(iid) or "Item #" .. iid
                            p[sid] = { id = iid, name = n, ilvl = 0 }
                        end
                    end
                end
            end
        end
        MeederSIMCharDB.bisLoaded3 = true
    end
end

function MeederSIM:GetActiveBiS()
    self:InitBiS()
    local tab = (self.bisFrame and self.bisFrame.activeTab) or "overall"
    return MeederSIMCharDB.bisProfiles[tab] or {}
end

local SLOT_NAME_TO_ID = {
    head=1,kopf=1,neck=2,hals=2,shoulder=3,schulter=3,
    back=15,chest=5,brust=5,wrist=9,handgelenke=9,
    hands=10,waist=6,taille=6,legs=7,beine=7,feet=8,
    ring1=11,ring2=12,trinket1=13,schmuck1=13,trinket2=14,schmuck2=14,
    mainhand=16,haupthand=16,weapon=16,offhand=17,nebenhand=17,
}

function MeederSIM:SwapDualSlotBiS(slotId, newId)
    local bis = self:GetActiveBiS()
    local other
    if slotId == 11 then other = 12 elseif slotId == 12 then other = 11
    elseif slotId == 13 then other = 14 elseif slotId == 14 then other = 13
    else return end
    if bis[other] and bis[other].id == newId then bis[other] = bis[slotId] end
end

function MeederSIM:SetBiSCmd(args)
    self:InitBiS()
    local cl = args:match("|c%%x+|Hitem:.-|h%%[.-%]|h|r")
    if cl then return self:SetBiSFromLink(cl) end
    local sn, ids = args:match("^(%%a+)%%s+(%%d+)")
    if sn and ids then
        local sid = SLOT_NAME_TO_ID[sn:lower()]
        if sid then return self:SetBiSFromId(tonumber(ids), sid) end
    end
    local iid = args:match("^(%%d+)$")
    if iid then return self:SetBiSFromId(tonumber(iid)) end
    if args == "" then
        local ok, _, lk = pcall(function() return GameTooltip:GetItem() end)
        if ok and lk then return self:SetBiSFromLink(lk) end
    end
    self:Print("/msim setbis <id> oder hover + eingeben")
end

function MeederSIM:SetBiSFromLink(link)
    if not link then return end
    local sid = self:GetSlotForItem(link)
    if not sid then self:Print("Slot nicht erkannt."); return end
    local n, _, _, il = GetItemInfo(link)
    local id = tonumber(link:match("item:(%%d+)"))
    self:SwapDualSlotBiS(sid, id)
    self:GetActiveBiS()[sid] = { id = id, name = n or "?", ilvl = il or 0 }
    self:Print("|cff00ff00BiS gesetzt:|r " .. (SLOT_DISPLAY[sid] or sid) .. " = " .. (n or "?"))
end

function MeederSIM:SetBiSFromId(itemId, slotId)
    if not itemId or itemId == 0 then self:Print("Ungueltie ID."); return end
    local n, il2, _, il = GetItemInfo(itemId)
    if not n then
        self:Print("Lade Item...")
        if C_Item and C_Item.RequestLoadItemDataByID then C_Item.RequestLoadItemDataByID(itemId) end
        C_Timer.After(1, function() self:SetBiSFromId(itemId, slotId) end)
        return
    end
    if not slotId then
        slotId = self:GetSlotForItem(il2 or ("item:" .. itemId))
        if not slotId then self:Print("Slot nicht erkannt. /msim setbis <slot> " .. itemId); return end
    end
    self:SwapDualSlotBiS(slotId, itemId)
    self:GetActiveBiS()[slotId] = { id = itemId, name = n, ilvl = il or 0 }
    self:Print("|cff00ff00BiS gesetzt:|r " .. (SLOT_DISPLAY[slotId] or slotId) .. " = " .. n)
end

function MeederSIM:ClearBiS()
    MeederSIMCharDB.bisProfiles = nil
    MeederSIMCharDB.bisLoaded = nil
    MeederSIMCharDB.bisLoaded2 = nil
    MeederSIMCharDB.bisLoaded3 = nil
    MeederSIMCharDB.bisMigrated = nil
    MeederSIMCharDB.bis = nil
    self:Print("BiS komplett gelöscht. /reload für neue Defaults.")
end

function MeederSIM:ExportBiS()
    local bis = self:GetActiveBiS()
    if not next(bis) then self:Print("Keine BiS-Items."); return end
    local parts = {}
    for s, d in pairs(bis) do if d.id then parts[#parts+1] = s..":"..d.id end end
    self:Print("MEEDERBIS:" .. table.concat(parts, ","))
end

function MeederSIM:ImportBiS(str)
    if not str or str == "" then self:Print("/msim importbis <string>"); return end
    self:InitBiS()
    local c = 0
    for s, id in str:gmatch("(%%d+):(%%d+)") do
        s, id = tonumber(s), tonumber(id)
        local n = GetItemInfo(id) or "Item #"..id
        self:GetActiveBiS()[s] = { id = id, name = n, ilvl = 0 }
        c = c + 1
    end
    self:Print(c .. " Items importiert.")
end

function MeederSIM:IsItemBiS(itemId, slotId, class, spec)
    if not itemId or itemId == 0 then return false end
    self:InitBiS()
    local bis = self:GetActiveBiS()
    if bis[slotId] and bis[slotId].id == itemId then return true end
    if slotId == 11 or slotId == 12 then
        local b1, b2 = bis[11], bis[12]
        if (b1 and b1.id == itemId) or (b2 and b2.id == itemId) then return true end
    end
    if slotId == 13 or slotId == 14 then
        local b1, b2 = bis[13], bis[14]
        if (b1 and b1.id == itemId) or (b2 and b2.id == itemId) then return true end
    end
    return false
end

function MeederSIM:CheckBiSAllSpecs(itemId, slotId)
    if self:IsItemBiS(itemId, slotId) then return { [self.spec or "Spec"] = true } end
    return nil
end
